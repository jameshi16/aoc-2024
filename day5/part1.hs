predicate :: [Int] -> Bool
predicate [] = True
predicate (x:xs) = predicate' x xs && predicate xs

getMiddleElement :: [Int] -> Int
getMiddleElement xs = xs !! fromIntegral (floor ((int2Double $ length xs) / 2.0))

solution :: [[Int]] -> Int
solution xs = sum $ map getMiddleElement $ filter predicate xs
